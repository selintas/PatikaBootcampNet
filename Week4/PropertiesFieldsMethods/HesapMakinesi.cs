using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace PropertiesFieldsMethods
{
    public class HesapMakinesi 
    {
        //Method (Metod)
        // Method: İki sayıyı toplama
        public int Topla(int sayi1, int sayi2)
        {
            return sayi1 + sayi2;
        }

        // Method: İki sayıyı çarpma
        public int Carp(int sayi1, int sayi2)
        {
            return sayi1 * sayi2;
        }
    }
} // Method'lar, sınıfın veya nesnenin davranışlarını tanımlar ve bu davranışları gerçekleştirmelerini sağlar.
